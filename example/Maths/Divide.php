namespace Maths;

/**
 * Function: Divide
 * =============================================================================
 * This will divide the first number by the second.
 * 
 * Parameters:
 * -----------------------------------------------------------------------------
 * $a - This is number 1
 * $b - This is number 2
 * 
 * Returns:
 * -----------------------------------------------------------------------------
 * The result of $a / $b
 */
function Divide($a, $b)
{
	return $a / $b;
}
